export const SUBJECT_DOMAINS: Record<string, SubjectDomain> = {
  kokugo: KOKUGO,
  sugaku: SUGAKU,
  eigo: EIGO,
  rika: RIKA,
  shakai: SHAKAI,
};

export function isMidtermScope(topicSlug: string): boolean {
  for (const domain of Object.values(SUBJECT_DOMAINS)) {
    if (domain.midtermTopicSlugs.includes(topicSlug)) return true;
  }
  return false;
}
